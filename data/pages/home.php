<h2>About NanoCMS v0.4 - final</h2>
<p>
	NanoCMS is the tiniest CMS you can find around...:)<br>
The user interface and the functionality are very very simple and easy to use.<br>
NanoCMS is filebased, so no database is required, making it easy to install <br>
<br>
  With this CMS you can create pages without actually touching any code.<br>
  You can manage website pages very easily and the links in the sidebar are generated automatically...<br>
  <br>
If you dont like the design it is very easy to make / port / integrate new designs.</p>
<p>NanoCMS is programmed by <a href="http://kalyanchakravarthy.net">Kalyan Chakravarthy</a> </p>

